const t = (lang, key) => key.split('.').reduce((obj, segment) => obj?.[segment], translations[lang]) || '';

const applyTranslations = (lang) => {
    document.querySelectorAll('[data-i18n-key]').forEach((element) => {
        const translation = t(lang, element.dataset.i18nKey);
        if (!translation) {
            return;
        }

        if (element.hasAttribute('data-i18n-html')) {
            element.innerHTML = translation;
        } else {
            element.textContent = translation;
        }
    });

    document.documentElement.lang = lang;
    document.title = t(lang, 'pageTitle') || document.title;
    localStorage.setItem('resume-lang', lang);
};

document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        document.querySelector(this.getAttribute('href')).scrollIntoView({
            behavior: 'smooth'
        });
    });
});

const langToggleBtn = document.getElementById('lang-toggle');
const savedLang = localStorage.getItem('resume-lang') || 'en';
applyTranslations(savedLang);

langToggleBtn.addEventListener('click', () => {
    const newLang = document.documentElement.lang === 'en' ? 'ru' : 'en';
    applyTranslations(newLang);
});
